#En terminal se corre el comando:

rails new MySite

#Esto crea una nueva app e rails

#El siguiente comando instala todas las librerias (gemas) que utilizaremos para nuestra app

bundle install


#Con este comando levantamos el servicio:


rails server


#----------

#El siguiente comando, nos permite crear un controlador:

rails generate controller Pages

#El siguiente comando, nos permite crear un modelo:

rails generate model Message
